//Örnek 2.3. Mevsimleri veren uygulamayı enum sabiti kullanarak kodlayınız.
enum Mevsim : Int {
    case Yaz=1
    case SonBahar
    case Kis
    case IlkBahar 
}

print ("Mevsimler..:\n")
print ("Havalar Cok Soguk:\(Mevsim.Kis)\n");
print ("Havalar Cok Sicak:\(Mevsim.Yaz)\n");
print ("Çiçekler açtı:\(Mevsim.IlkBahar)\n");
print ("Yapraklar dökülüyor:\(Mevsim.SonBahar)\n");
print ("Kış sırası:\(Mevsim.Kis.rawValue)\n");

/*Output:
Havalar Cok Soguk: Kis
Havalar Cok Sicak: Yaz
Çiçekler açtı: IlkBahar
Yapraklar dökülüyor: SonBahar
Kış sırası: 3
*/
